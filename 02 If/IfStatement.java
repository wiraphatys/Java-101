public class IfStatement {
    public static void main(String[] args) {
        int x = 10;
        int y = 15;
        if (x > y) {
            System.out.println("x > y");
        } else if (x == y) {
            System.out.println("x = y");
        } else {
            System.out.println("x < y");
        }
    }
}
